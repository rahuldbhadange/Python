# Types of Inheritance:
#     1.Single Inheritance
#     2.Multiple Inheritance
#     3.Multi-level Inheritance
#     4.Hierarchical Inheritance
#     5.Hybrid Inheritance
#
# 1.Single Inheritance: A derived class with single base class
#
# 2.Multiple Inheritance: A derived class with multiple base classes
#
# 3.Multi-Level Inheritance: Deriving a class from Another derived class
#
# 4.Hierarchical Inheritance: A base class with multiple derived classes
#
# 5.Hybrid Inheritance: Combination of 2 or more Inheritance
