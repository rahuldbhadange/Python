# Program illustrating Arithmetic operators

x=10
y=4

add=x+y
print("sum=",add)

sub=x-y
print("diff=",sub)

mul=x*y
print("product=",mul)

div=x/y
print("division=",div)

moddiv=x%y
print("modulo division=",moddiv)

floordiv=x//y
print("floor division=",floordiv)

exp=x**y
print("exponent=",exp)

print(10/3)
print(10%3)
print(10//3)
