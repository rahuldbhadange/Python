#Continue stmt: It is used in the loops
#when continue stmt is used in the loop, then it skips the execution of
#current iteration and Ctrl goes to next iteration

x=0
while(x<=10):
    x=x+1
    if(x==5):  
        continue   # here iteration 5 is skipped
    print(x,"hello")
    
    
        
print("end")
 
